# üî• –í–∞–∂–Ω–æ! –§—É–Ω–∫—Ü–∏—è sorted –º–æ–∂–µ—Ç –ø—Ä–∏–Ω–∏–º–∞—Ç—å –Ω–µ —Ç–æ–ª—å–∫–æ —Å–ø–∏—Å–∫–∏, –Ω–æ –∏ –¥—Ä—É–≥–∏–µ
# –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏: —Å—Ç—Ä–æ–∫–∏, –º–Ω–æ–∂–µ—Å—Ç–≤–∞, –∫–æ—Ä—Ç–µ–∂–∏, —Å–ª–æ–≤–∞—Ä–∏ –∏ —Ç.–ø.. –ü—Ä–∏ —ç—Ç–æ–º
# —Ñ—É–Ω–∫—Ü–∏—è –≤—Å–µ–≥–¥–∞ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–∏—Å–æ–∫.

my_list = [4, 8, 2, 9, 1, 7, 2]
sort_list = sorted(my_list)
print(my_list, sort_list, sep='\n')
rev_list = sorted(my_list, reverse=True)
print(my_list, rev_list, sep='\n')

# –ú–µ—Ç–æ–¥ sort –æ—Å—É—â–µ—Å—Ç–≤–ª—è–µ—Ç —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫—É —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Å–ø–∏—Å–∫–∞ –±–µ–∑ —Å–æ–∑–¥–∞–Ω–∏—è –∫–æ–ø–∏–∏, inplace.

my_list = [4, 8, 2, 9, 1, 7, 2]
my_list.sort()
